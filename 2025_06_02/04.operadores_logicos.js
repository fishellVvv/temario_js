let a = 10;
let b = 20;

// O (or)
console.log(true || false)

// Y (and)
console.log(true && false)

// No (not)
console.log(!false)

// Igual
console.log(a == b)

// Extrictamente igual
console.log(5 === "5")

// Desigual
console.log(a != b)

// Extrictamente desigual
console.log(5 !== "5")

// Mayor - Mayor o igual
console.log(a > b)
console.log(a >= b)

// Menor - Menor o igual
console.log(a < b)
console.log(a <= b)
